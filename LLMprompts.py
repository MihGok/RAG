from __future__ import annotations

import json
from textwrap import dedent
from typing import Any


class PromptBank:
    @staticmethod
    def clarifying_questions(topic: str, user_goal: str = "", level_hint: str = "") -> str:
        goal_block = f"\nЦель пользователя: {user_goal}" if user_goal else ""
        level_block = f"\nПодсказка по уровню: {level_hint}" if level_hint else ""
        return dedent(f"""
        Ты — IT-методист и архитектор персональных образовательных треков.
        Твоя задача — помочь пользователю уточнить запрос для поиска релевантных материалов в базе онлайн-курсов (Stepik).
        
        Пользователь хочет найти образовательные материалы по теме: "{topic}".{goal_block}{level_block}

        Сгенерируй 3–5 уточняющих вопроса на русском языке. 
        
        ЖЕСТКИЕ ПРАВИЛА:
        1. Формат обучения ВСЕГДА онлайн-курсы. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО спрашивать про желаемый формат (видео, статьи, вебинары и т.д.).
        2. Фокусируйся ТОЛЬКО на технологическом стеке, конкретных фреймворках, текущем уровне знаний и узкой предметной области.
        3. Вопросы должны мотивировать пользователя назвать конкретные термины, которые потом пойдут в векторный поиск (RAG).
        4. Формулируй вопросы как эксперт: дружелюбно, но профессионально.

        ПРИМЕР ХОРОШИХ ВОПРОСОВ (если тема "Машинное обучение"):
        - "С какими библиотеками вы уже работали или планируете работать (например, scikit-learn, PyTorch, TensorFlow)?"
        - "Как вы оцениваете свою математическую базу (линейная алгебра, статистика) — нужно ли включить модули для её повторения?"
        - "Для каких задач вы планируете применять ML: анализ табличных данных, компьютерное зрение (CV) или обработка текста (NLP)?"
        
        Верни ТОЛЬКО валидный JSON по заданной схеме без лишнего текста.
        """).strip()

    @staticmethod
    def pipeline_setup(topic: str, user_answers: str) -> str:
        return dedent(f"""
        Ты — AI-ассистент, формирующий метаданные для RAG-системы поиска по курсам Stepik.
        
        Тема обучения: "{topic}"
        Ответы пользователя на уточняющие вопросы:
        {user_answers}

        На основе этой информации создай:

        1. Несколько поисковых запроса (на русском языке), оптимизированных для семантического поиска.
        Они должны как минимум включать, но не ограничиваться:
           - Широкий (охватывает общую область).
           - Точный (включает конкретные технологии из ответов).
           - Практико-ориентированный (направлен на решение задачи пользователя).

        2. Подробную карту тегов для классификации материала. Категории:
           - основные_концепции
           - инструменты_и_технологии
           - практические_навыки
           - смежные_темы
           - уровень_сложности

        ПРИМЕР РЕЗУЛЬТАТА ДЛЯ ТЕГОВ (если тема Backend Python):
        инструменты_и_технологии: ["FastAPI", "SQLAlchemy", "PostgreSQL", "Docker", "Pydantic"]
        практические_навыки: ["проектирование REST API", "настройка CI/CD", "написание unit-тестов"]

        ЖЁСТКИЕ ПРАВИЛА:
        - Запросы и теги должны строго соответствовать ответам пользователя.
        - Избегай «воды» (слова типа "обучение", "курс", "как сделать"). Оставляй только суть и термины.
        - Верни ТОЛЬКО валидный JSON по заданной схеме.
        """).strip()

    @staticmethod
    def course_ranking(topic: str, user_context: str, courses: list[dict[str, Any]]) -> str:
        return dedent(f"""
        Ты — эксперт по оценке образовательного контента (платформа Stepik).
        
        Тема поиска: "{topic}"
        Контекст и уровень пользователя:
        {user_context}

        Оцени релевантность каждого курса из списка ниже по шкале 1–10.

        КРИТЕРИИ ОЦЕНКИ:
        1. Соответствие стеку и уровню (Главный вес). Если курс для новичков, а юзер просит продвинутый уровень (или наоборот) — снижай балл.
        2. Актуальность технологий.
        3. Популярность (число слушателей) — ВТОРИЧНЫЙ сигнал. Не давай 10 баллов устаревшему курсу только потому, что у него 50к зачислений.

        Список курсов для оценки:
        {json.dumps(courses, ensure_ascii=False, indent=2)}

        ПРИМЕР ОБОСНОВАНИЯ:
        "Идеально подходит под запрос: фокус на асинхронном программировании в Python, что соответствует заявленному уровню Middle."

        ЖЁСТКИЕ ПРАВИЛА:
        - Оцени КАЖДЫЙ курс из списка (не пропускай).
        - Обоснование — максимум 1-2 предложения (строго по сути).
        - Верни ТОЛЬКО валидный JSON по заданной схеме.
        """).strip()

    @staticmethod
    def lesson_merge(cluster_id: str, lesson_titles: list[str], short_snippets: list[str] | None = None) -> str:
        snippets_block = ""
        if short_snippets:
            snippets_block = "\nФрагменты уроков:\n" + "\n".join(
                f"- {t}: {s}" for t, s in zip(lesson_titles, short_snippets)
            )

        return dedent(f"""
        Ты — редактор базы знаний для образовательного RAG-ассистента. 
        Перед тобой кластер уроков с похожими названиями и содержанием. Твоя цель — спроектировать "идеальный" канонический урок на их основе.

        cluster_id: {cluster_id}
        Названия исходных уроков: {json.dumps(lesson_titles, ensure_ascii=False)}{snippets_block}

        ЗАДАЧА:
        - Сформулировать емкое `merged_title` (например, вместо "Введение в циклы", "Циклы for", "Циклы 1" -> "Циклы в Python: for и while").
        - Составить `merged_summary` — краткое резюме того, о чем этот сборный урок.
        - Выделить `merged_key_points` — список основных концепций без дублирования.
        - Выделить `merged_tags`.

        ЖЁСТКИЕ ПРАВИЛА:
        - Не фантазируй и не добавляй темы, которых нет в названиях/сниппетах.
        - Верни ТОЛЬКО валидный JSON по заданной схеме.
        """).strip()

    @staticmethod
    def lesson_text_merge(title: str, lesson_texts: list[str], lesson_ids: list[str] | None = None) -> str:
        ids_block = f"\nlesson_ids = {json.dumps(lesson_ids, ensure_ascii=False)}" if lesson_ids else ""
        texts_block = "\n".join(f"### Урок {i+1}\n{text}" for i, text in enumerate(lesson_texts))

        return dedent(f"""
        Ты — алгоритм компрессии текста для создания эмбеддингов в RAG-системе.
        Объедини тексты нескольких связанных уроков в один плотный, структурированный и информативный чанк (конспект).

        Тема кластера: "{title}"{ids_block}

        ИСХОДНЫЕ ТЕКСТЫ:
        {texts_block}

        ТРЕБОВАНИЯ К КОНСПЕКТУ (summary):
        - Удали все приветствия, «воду», просьбы поставить лайк или подписаться на курс.
        - Сохрани ВСЕ важные определения, фрагменты кода (если есть), математические формулы и списки свойств.
        - Объедини дублирующуюся информацию (если в двух уроках объясняется одно и то же, оставь лучшее объяснение).
        - Структурируй результат так, чтобы LLM в будущем могла легко извлекать из него факты.

        Верни ТОЛЬКО валидный JSON по схеме summary, включающий:
        - сам конспект (summary)
        - ключевые поинты (key_points)
        - затронутые темы (topics)
        - уровень сложности (difficulty_level)
        - язык (language)
        """).strip()

